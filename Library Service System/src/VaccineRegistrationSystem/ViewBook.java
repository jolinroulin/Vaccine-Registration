    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)jTableBook.getModel();
        
        if(jTableBook.getSelectedRowCount()==1){
            tblModel.removeRow(jTableBook.getSelectedRow());
            JOptionPane.showMessageDialog(this, "The data is deleted from the table.");
            saveBookDetails();
        }else{
            if (jTableBook.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Table is Empty.");
            }else{
                JOptionPane.showMessageDialog(this, "Please select single row for delete.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTableBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableBookMouseClicked
        DefaultTableModel tblModel = (DefaultTableModel)jTableBook.getModel();
        
        //set data to textfield when row i selected
        String tblId = tblModel.getValueAt(jTableBook.getSelectedRow(), 0).toString();
        String tblName = tblModel.getValueAt(jTableBook.getSelectedRow(), 1).toString();
        String tblAuthor = tblModel.getValueAt(jTableBook.getSelectedRow(), 2).toString();
        String tblPublisher = tblModel.getValueAt(jTableBook.getSelectedRow(), 3).toString();
        String tblType = tblModel.getValueAt(jTableBook.getSelectedRow(), 4).toString();
        String tblPrice = tblModel.getValueAt(jTableBook.getSelectedRow(), 5).toString();
        String tblQuantity = tblModel.getValueAt(jTableBook.getSelectedRow(), 6).toString();
        String tblAvailable = tblModel.getValueAt(jTableBook.getSelectedRow(), 7).toString();
        
        txtBookId.setText(tblId);
        txtBookName.setText(tblName);
        txtBookPublisher.setText(tblPublisher);
        txtBookAuthor.setText(tblAuthor);
        cmbBookType.setSelectedItem(tblType);
        txtBookPrice.setText(tblPrice);
        txtBookQuantity.setText(tblQuantity);
        txtBookAvailable.setText(tblAvailable);
        
    }//GEN-LAST:event_jTableBookMouseClicked

    private void btnBookUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookUpdateActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)jTableBook.getModel();
        if(jTableBook.getSelectedRowCount()==1){
            String tblId = txtBookId.getText();
            String tblName = txtBookName.getText();
            String tblAuthor = txtBookAuthor.getText();
            String tblPublisher = txtBookPublisher.getText();
            String tblBookType = cmbBookType.getSelectedItem().toString();
            String tblBookPrice = txtBookPrice.getText();
            String tblBookQuantity = txtBookQuantity.getText();
            String tblBookAvailable = txtBookAvailable.getText();

            tblModel.setValueAt(tblId,jTableBook.getSelectedRow(), 0);
            tblModel.setValueAt(tblName,jTableBook.getSelectedRow(), 1);
            tblModel.setValueAt(tblAuthor,jTableBook.getSelectedRow(), 2);
            tblModel.setValueAt(tblPublisher,jTableBook.getSelectedRow(), 3);
            tblModel.setValueAt(tblBookType ,jTableBook.getSelectedRow(), 4);
            tblModel.setValueAt(tblBookPrice ,jTableBook.getSelectedRow(), 5);
            tblModel.setValueAt(tblBookQuantity,jTableBook.getSelectedRow(), 6);
            tblModel.setValueAt(tblBookAvailable,jTableBook.getSelectedRow(), 7);

            JOptionPane.showMessageDialog(this, "Update Successfully.");
            saveBookDetails();
        }else{
            if (jTableBook.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Table is Empty.");
            }else{
                JOptionPane.showMessageDialog(this, "Please select single row for update.");
            }
        }       
    }//GEN-LAST:event_btnBookUpdateActionPerformed

