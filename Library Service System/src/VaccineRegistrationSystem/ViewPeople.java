        txtPassword.setText(tblPassword);
        cmbGender.setSelectedItem(tblGender);
        txtName.setText(tblName);
        txtEmail.setText(tblEmail);
        txtDOB.setText(tblDOB);
        txtContactNumber.setText(tblContactNo);
