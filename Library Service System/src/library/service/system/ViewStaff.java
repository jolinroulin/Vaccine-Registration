    private void ImportMemberDetailsToTable() {
        String filepath = "C:\\Users\\User\\Documents\\NetBeansProjects\\Library Service System\\StaffData.txt";
        File file = new File(filepath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            DefaultTableModel model = (DefaultTableModel) jTableMember.getModel();
            /*String[] columnsName = firstLine.split(" : ");
            
            model.setColumnIdentifiers(columnsName);*/

            Object[] tableLines = br.lines().toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(" : ");
                model.addRow(dataRow);
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void saveStaffDetails(){
                String filePath = "C:\\Users\\User\\Documents\\NetBeansProjects\\Library Service System\\StaffData.txt";
      File file = new File(filePath);
      try {
          FileWriter fw = new FileWriter(file);
          BufferedWriter bw = new BufferedWriter(fw);
          
          for(int i = 0; i < jTableMember.getRowCount(); i++){
              for(int j = 0; j < jTableMember.getColumnCount(); j++){
                  bw.write(jTableMember.getValueAt(i,j).toString()+ " : " );
              }
              bw.newLine();
      }
          bw.close();
          fw.close();
      }
      catch (IOException ex) {
        JOptionPane.showMessageDialog(null, "Fail to save into text file!");
      }
    }
    
